package helpers;

public class DataCouple {
	

		private String username;
		private int score;
		
		public DataCouple(String username, int score){
			this.username = username;
			this.score = score;
		}
		
		public String getUsername(){
			return username;
		}
		 public int getScore(){
			 return score;
		 }
		
	
}
